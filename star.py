import turtle
turtle.Screen().bgcolor("salmon")
t = turtle.Turtle()

# First triangle
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.penup()

t.right(150)

t.forward(50)


# second triangle for star

t.pendown()

t.right(90)

t.forward(100)

t.right(120)

t.forward(100)

t.right(120)

t.forward(100)

turtle.done()